#Declaració de variables
x = input("Dis-me el número X: ")
y = input("Dis-me el número Y: ")

#Contingut de las variables
print(f'El valor de x és: {x}')
print(f'El valor de y és: {y}')

#Operacions amb les variables.
print(f'La suma de x i y és: {x + y}')
print(f'La resta de x i y és: {x - y}')
print(f'La multiplicació de x i y és: {x * y}')
print(f'La divisió de x i y és: {x / y}')
print(f'La divisió sencera de x i y és: {x // y}')
print(f'La resta de la divisió sencera de x i y és: {x % y}')
